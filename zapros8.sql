/*5*/
CREATE VIEW дХЯЖХОКХМШ
AS SELECT TOP 10 SUBJECT, SUBJECT_NAME, PULPIT
FROM SUBJECT
ORDER BY SUBJECT_NAME;
SELECT * FROM дХЯЖХОКХМШ;

/*6*/
ALTER VIEW [йнкхвеярбн йютедп] WITH SCHEMABINDING
AS SELECT FACULTY.FACULTY_NAME, COUNT(PULPIT.PULPIT) AS йнкхвеярбн_йютедп
FROM dbo.FACULTY FACULTY INNER JOIN dbo.PULPIT PULPIT
ON FACULTY.FACULTY = PULPIT.FACULTY
GROUP BY FACULTY.FACULTY_NAME;
