use univers;

/*1*/
CREATE VIEW опеондюбюрекэ 
AS SELECT TEACHER, TEACHER_NAME, GENDER, PULPIT
FROM TEACHER
SELECT * FROM опеондюбюрекэ

/*2*/
CREATE VIEW [йнкхвеярбн йютедп]
AS SELECT FACULTY.FACULTY_NAME, COUNT(PULPIT.PULPIT) AS йнкхвеярбн 
FROM FACULTY INNER JOIN PULPIT 
ON FACULTY.FACULTY = PULPIT.FACULTY 
GROUP BY FACULTY.FACULTY_NAME
SELECT * FROM [йнкхвеярбн йютедп]

